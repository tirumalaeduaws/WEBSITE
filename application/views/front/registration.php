<script type="text/javascript">
var save_method;
function save()
{
    save_method = 'add';
    $('#btnSave').attr('disabled',true); 
    $('#btnSave').text('Submitting...'); 
    var url;
    var formulario = new FormData($('#form').get(0));    
    if(save_method == 'add') { url = "<?php echo base_url('register/saveregisterform')?>"; }
    $.ajax({
        url : url,
        type: "POST",
        data: formulario,
        processData: false,
        contentType: false,
        dataType: "JSON",
        success: function(data)
        {
            if(data.status)
            {

                $('#mob').val(data.phone);
                $('#tidnum').val(data.iid);
                $('#btnSave').attr('disabled',true); 
                $('#btnVerify').attr('disabled',false);
                $('#t_otp').attr('readonly',false);
                $("#form :input").prop("disabled", true);
                $('#omsg').text(data.msg);
            }else{
                $('#errors').html(data.msg); 
                $('#errorModal').modal('show');
            }
            $('#btnSave').text('SUBMIT APPLICATION');
            $('#btnSave').attr('disabled',false);
        },
        error: function (jqXHR, textStatus, errorThrown)
        {

          console.log(jqXHR);
          console.log(textStatus);
          console.log(errorThrown);

            alert('Error!  Something Wrong');
            $('#btnSave').text('SUBMIT APPLICATION'); 
            $('#btnSave').attr('disabled',false); 
        }
    });
}
</script>
